/**
 * Atividade prática 4
 * Faça um programa que imprima todos os números de 0 a 100.
 */
public class CBBAtividadePratica4 {
    public static void main(String[] args) {
        int numero = 0;

        while (numero <= 100) {
            System.out.println(numero);
            numero++;
        }
    }
}
