/**
 * 10. Reescreva o programa anterior utilizando o comando do-while.
 */
public class BMExercicio10 {
    public static void main(String[] args) {
        int numero = 10;
        int contador = 20;

        do {
            System.out.print(numero + "  ");
            contador--;
        } while (contador > 0);
    }
}
